List<AssetAnimalModel> assetAnimals = [];

class AssetAnimalModel {
  String animalVirtualImage;
  String animalVoice;
  String animalType;

  AssetAnimalModel({
    required this.animalVirtualImage,
    required this.animalVoice,
    required this.animalType,
  });
}

void generateAssetAnimal() {
  for (int i = 0; i < assetAnimalVirtualImages.length; i++) {
    AssetAnimalModel animalModel = AssetAnimalModel(
      animalVirtualImage: assetAnimalVirtualImages[i],
      animalVoice: assetAnimalVoices[i],
      animalType: Platform.localeName.split("_")[0] == "tr"
          ? assetTr[i]
          : Platform.localeName.split("_")[0] == "id"
              ? assetId[i]
              : Platform.localeName.split("_")[0] == "hi"
                  ? assetHi[i]
                  : Platform.localeName.split("_")[0] == "it"
                      ? assetIt[i]
                      : Platform.localeName.split("_")[0] == "de"
                          ? assetDe[i]
                          : Platform.localeName.split("_")[0] == "pt"
                              ? assetPt[i]
                              : Platform.localeName.split("_")[0] == "ru"
                                  ? assetRu[i]
                                  : assetEn[i],
    );
    assetAnimals.add(animalModel);
  }
}
